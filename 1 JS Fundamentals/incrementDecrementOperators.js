let a = 10;
console.log(a++); // 10 - returns the value and then increments
console.log(a); // 11 - incremented value
console.log(a--); // 11 - returns the value and then decrements
console.log(a); // 10 - decremented value
console.log(++a); // 11 - increments the value and then returns
console.log(--a); // 10 - decrements the value and then returns
